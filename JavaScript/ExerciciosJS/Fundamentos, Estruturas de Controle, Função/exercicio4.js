/*Crie uma função que irá receber dois valores, o dividendo e o divisor. A função deverá imprimir o resultado
e o resto da divisão destes dois valores.*/

function divisao(x, y, total, resto){
    console.log(`O resultado da divisão é ${Math.floor(total = x / y)}`)
    console.log(`Ò resto da divisão é ${resto = x % y}`)
}

divisao(10, 5)
divisao(29, 6)